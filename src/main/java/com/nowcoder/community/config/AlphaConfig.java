/**
 * @author ：xuwandong
 * @date ：Created in 2022/9/9 16:27
 * @description：
 * @modified By：
 */
@Configuration
public class AlphaConfig {

    @Bean
    public SimpleDateFormat simpleDateFormat(){
        return new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
    }

}
